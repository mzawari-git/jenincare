// ==================================================================
// Apply replacements with longest-first sorting per file
// ==================================================================
$changed = 0;
foreach ($replacements as $theme => $pairs) {
    $path = $files[$theme];
    echo "Processing: $theme\n";

    $content = file_get_contents($path);
    if ($content === false) { echo "  FAILED\n"; continue; }
    $original = $content;

    // Sort by old-string length descending to prevent substring matches
    usort($pairs, fn($a, $b) => strlen($b[0]) <=> strlen($a[0]));

    $search = array_map(fn($p) => $p[0], $pairs);
    $replace = array_map(fn($p) => $p[1], $pairs);

    $content = str_replace($search, $replace, $content);

    // Check for remaining ? outside Blade comments
    $remaining = 0;
    $stripped = preg_replace('/\{\{--.*?--\}\}/s', '', $content);
    $stripped = preg_replace('/\?\.[a-z]/', '', $stripped); // ignore JS optional chaining
    $matches = [];
    preg_match_all('/\?{3,}/', $stripped, $matches);
    $remaining = array_sum(array_map('strlen', $matches[0] ?? []));

    if ($content !== $original) {
        file_put_contents($path, $content);
        echo "  Saved. Remaining ? chars: $remaining\n";
        $changed++;
    } else {
        echo "  No changes\n";
    }
}

echo "\nDone! $changed of " . count($files) . " files modified.\n";
